function add(num1,num2){
    let res = num1 + num2
    return res
};

console.log(add(57,96));


function subtract(num1,num2){
    let res = num1 - num2
    return res
};

console.log(subtract(100,80));

function multiply(num1, num2){
    let res = num1 * num2
    return res
};

console.log(multiply(9,9));

function divide(num1,num2){
    let res = num1 / num2
    return res
};

console.log(divide(96,12));